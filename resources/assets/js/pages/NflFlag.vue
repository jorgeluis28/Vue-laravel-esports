<script>
    import pfetch from "../pfetch/pfetch";
    export default {
        name: "NflFlag",
        data(){
            return{
                access_token:null,
                leagueId:85
            }
        },
        created() {
            this.loadToken();
        },
        methods:{
            async loadToken(){
                const url = 'api/token';
                const res = await pfetch(url);
                if(res.token){
                    this.access_token = res.token;
                }
            },
            joinLeague(){
                try{
                    if (!this.access_token) {
                        window.location.href = '/signin_with_doctorbattles';
                        return;
                    }
                    let postData = {
                        league_id: this.leagueId
                    };
                    const url = 'https://doctorbattles.com/api/v1/auto_join_league';
                    console.log(this.access_token);
                    $.ajax({
                        url: url,
                        method: 'POST',
                        dataType: 'json',
                        data: postData,
                        headers: {
                            'Authorization': 'Bearer ' + this.access_token,
                        },
                        success: function(res) {
                            if(res.status != 'OK') {
                                if(res.response_txt){
                                    alert(res.response_txt);
                                }else{
                                    console.log(res);
                                }
                            }else if(res.response_txt != ''){
                                alert(res.response_txt)
                            }
                        },
                        error: function(e) {
                            console.log(e);
                            if(e.status === 401){
                                window.location.href = '/signin_with_doctorbattles';
                            }
                        }
                    });
                }catch (e) {
                    console.log(e);
                }
            },
            createAutoTeam(postData, callback) {
                let data = {
                    team_name: postData.team_name,
                    platform_id: 0,
                    game_id: 0,
                    region_id: 0,
                    video_link: "",
                    bio_content: "",
                    file_name: null,
                    banner_image: null,
                    group_id: 0,
                    proceed: 1,
                    league_id: postData.league_id
                };

                $.ajax({
                    url: 'https://doctorbattles.com/api/v1/team_mg/child/join_auto_team',
                    method: 'POST',
                    dataType: 'JSON',
                    data: data,
                    headers: {
                        'Authorization': 'Bearer ' + access_token
                    },
                    success: function(res) {
                        if(res.status == 'OK') {
                            callback();
                        }
                    },
                    error: function() {}
                })
            }
        }
    }
</script>

